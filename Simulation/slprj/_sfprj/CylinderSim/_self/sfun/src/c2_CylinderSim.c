static void mdlStart_c2_CylinderSim(SimStruct *S)
{
  SFc2_CylinderSimInstanceStruct *chartInstance;
  ChartRunTimeInfo * crtInfo = (ChartRunTimeInfo *)utMalloc(sizeof
    (ChartRunTimeInfo));
  chartInstance = (SFc2_CylinderSimInstanceStruct *)utMalloc(sizeof
    (SFc2_CylinderSimInstanceStruct));
  memset(chartInstance, 0, sizeof(SFc2_CylinderSimInstanceStruct));
  if (chartInstance==NULL) {
    sf_mex_error_message("Could not allocate memory for chart instance.");
  }

  chartInstance->chartInfo.chartInstance = chartInstance;
  chartInstance->chartInfo.isEMLChart = 1;
  chartInstance->chartInfo.chartInitialized = 0;
  chartInstance->chartInfo.sFunctionGateway = sf_opaque_gateway_c2_CylinderSim;
  chartInstance->chartInfo.initializeChart = sf_opaque_initialize_c2_CylinderSim;
  chartInstance->chartInfo.terminateChart = sf_opaque_terminate_c2_CylinderSim;
  chartInstance->chartInfo.enableChart = sf_opaque_enable_c2_CylinderSim;
  chartInstance->chartInfo.disableChart = sf_opaque_disable_c2_CylinderSim;
  chartInstance->chartInfo.getSimState = sf_opaque_get_sim_state_c2_CylinderSim;
  chartInstance->chartInfo.setSimState = sf_opaque_set_sim_state_c2_CylinderSim;
  chartInstance->chartInfo.getSimStateInfo =
    sf_get_sim_state_info_c2_CylinderSim;
  chartInstance->chartInfo.zeroCrossings = NULL;
  chartInstance->chartInfo.outputs = NULL;
  chartInstance->chartInfo.derivatives = NULL;
  chartInstance->chartInfo.mdlRTW = mdlRTW_c2_CylinderSim;
  chartInstance->chartInfo.mdlStart = mdlStart_c2_CylinderSim;
  chartInstance->chartInfo.mdlSetWorkWidths = mdlSetWorkWidths_c2_CylinderSim;
  chartInstance->chartInfo.extModeExec = NULL;
  chartInstance->chartInfo.restoreLastMajorStepConfiguration = NULL;
  chartInstance->chartInfo.restoreBeforeLastMajorStepConfiguration = NULL;
  chartInstance->chartInfo.storeCurrentConfiguration = NULL;
  chartInstance->chartInfo.callAtomicSubchartUserFcn = NULL;
  chartInstance->chartInfo.callAtomicSubchartAutoFcn = NULL;
  chartInstance->chartInfo.debugInstance = sfGlobalDebugInstanceStruct;
  chartInstance->S = S;
  crtInfo->checksum = SF_RUNTIME_INFO_CHECKSUM;
  crtInfo->instanceInfo = (&(chartInstance->chartInfo));
  crtInfo->isJITEnabled = false;
  crtInfo->compiledInfo = NULL;
  ssSetUserData(S,(void *)(crtInfo));  /* register the chart instance with simstruct */
  init_dsm_address_info(chartInstance);
  init_simulink_io_address(chartInstance);
  if (!sim_mode_is_rtw_gen(S)) {
  }

  sf_opaque_init_subchart_simstructs(chartInstance->chartInfo.chartInstance);
  chart_debug_initialization(S,1);
}

void c2_CylinderSim_method_dispatcher(SimStruct *S, int_T method, void *data)
{
  switch (method) {
   case SS_CALL_MDL_START:
    mdlStart_c2_CylinderSim(S);
    break;

   case SS_CALL_MDL_SET_WORK_WIDTHS:
    mdlSetWorkWidths_c2_CylinderSim(S);
    break;

   case SS_CALL_MDL_PROCESS_PARAMETERS:
    mdlProcessParameters_c2_CylinderSim(S);
    break;

   default:
    /* Unhandled method */
    sf_mex_error_message("Stateflow Internal Error:\n"
                         "Error calling c2_CylinderSim_method_dispatcher.\n"
                         "Can't handle method %d.\n", method);
    break;
  }
}
